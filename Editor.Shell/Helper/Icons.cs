namespace Editor.Shell;

/// <summary>
/// Strongly-typed icon resolver. Converts icon-set enums to their kebab-case string names
/// used by the rendering layer.
/// <para>Usage: <c>Icon.From(Lucide.Github)</c> or <c>Icon.None</c></para>
/// </summary>
public static class Icon
{
    /// <summary>No icon. Use where a <c>string?</c> icon parameter is optional.</summary>
    public const string? None = null;

    /// <summary>Resolve a <see cref="Lucide"/> icon to its string name.</summary>
    public static string From(Lucide icon) => icon.ToIconName();

    /// <summary>Resolve a <see cref="Feather"/> icon to its string name.</summary>
    public static string From(Feather icon) => icon.ToIconName();

    /// <summary>Resolve a <see cref="Heroicon"/> icon to its string name.</summary>
    public static string From(Heroicon icon) => icon.ToIconName();

    /// <summary>Pass through a raw icon name string. Useful for custom or dynamically resolved icons.</summary>
    public static string Custom(string name) => name;
}

